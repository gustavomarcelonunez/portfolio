function buildKnowledgeGraph() {
  const svg = document.getElementById('kgSvg');
  if (!svg) return;

  const edgesGroup = document.getElementById('kgEdges');
  const nodesGroup = document.getElementById('kgNodes');

  // Limpiar grafo previo (necesario al regenerar por cambio de idioma)
  edgesGroup.innerHTML = '';
  nodesGroup.innerHTML = '';
  hideKgPopup();

  const lang = (typeof currentLang !== 'undefined' && kgNodeText[currentLang]) ? currentLang : 'es';
  const text = kgNodeText[lang];

  // Centro del SVG (responsive)
  const viewBox = svg.viewBox.baseVal;

  const cx = viewBox && viewBox.width
    ? viewBox.width / 2
    : svg.clientWidth / 2;

  const cy = viewBox && viewBox.height
    ? viewBox.height / 2
    : svg.clientHeight / 2;


  // Nodos satélite (propiedades fijas; label/desc vienen de kgNodeText)
  const satellitesBase = [
    { id: 'llm',      r: 16, type: 'n-entity',  delay: 0.4 },
    { id: 'conicet',  r: 22, type: 'n-concept', delay: 0.4 },
    { id: 'hermes',   r: 22, type: 'n-entity',  delay: 1.6 },
    { id: 'botgbif',  r: 16, type: 'n-entity',  delay: 2.4 },
    { id: 'aquamind', r: 20, type: 'n-entity',  delay: 2.0 },
    { id: 'gf',       r: 16, type: 'n-entity',  delay: 0.6 },
    { id: 'obisbot',  r: 16, type: 'n-entity',  delay: 0.2 },
    { id: 'uns',      r: 20, type: 'n-concept', delay: 1.2 },
    { id: 'cenpat',   r: 22, type: 'n-concept', delay: 0.8 },
    { id: 'graphrag', r: 16, type: 'n-concept', delay: 1.0 }
  ].map(function (n) {
    return Object.assign({}, n, text[n.id], { link: kgNodeLinks[n.id] });
  });

  // Distribución equilibrada en anillos
  const rings = [150, 180, 205];
  const rotation = Math.random() * 360;

  const satellites = satellitesBase.map((node, i, arr) => ({
    ...node,

    angle:
      rotation +
      (360 / arr.length) * i +
      (Math.random() * 24 - 12),

    dist: rings[i % rings.length]
  }));

  satellites.forEach(function (n) {
    const rad = n.angle * Math.PI / 180;
    n.x = Math.round(cx + n.dist * Math.cos(rad));
    n.y = Math.round(cy + n.dist * Math.sin(rad));
  });

  const me = Object.assign({ id: 'me', x: cx, y: cy, r: 36, type: 'n-hermes', delay: 0 }, text.me);
  const nodes = [me].concat(satellites);

  const edges = satellites.map(function (n) { return ['me', n.id]; });
  edges.push(['conicet', 'cenpat']);
  edges.push(['conicet', 'uns']);

  const svgNS = 'http://www.w3.org/2000/svg';
  const nodeMap = {};
  nodes.forEach(n => nodeMap[n.id] = n);

  edges.forEach(function (pair) {
    const a = nodeMap[pair[0]];
    const b = nodeMap[pair[1]];
    const line = document.createElementNS(svgNS, 'line');
    line.setAttribute('class', 'kg-edge');
    line.setAttribute('x1', a.x);
    line.setAttribute('y1', a.y);
    line.setAttribute('x2', b.x);
    line.setAttribute('y2', b.y);
    edgesGroup.appendChild(line);
  });

  const popup = document.getElementById('kgPopup');

  nodes.forEach(function (n) {
    const g = document.createElementNS(svgNS, 'g');
    g.setAttribute('class', 'kg-node-group');
    g.style.cursor = 'pointer';

    const pulse = document.createElementNS(svgNS, 'circle');
    const pulseColorClass = n.type === 'n-hermes' ? 'p-hermes' : (n.type === 'n-entity' ? 'p-entity' : 'p-concept');
    pulse.setAttribute('class', 'kg-pulse ' + pulseColorClass);
    pulse.setAttribute('cx', n.x);
    pulse.setAttribute('cy', n.y);
    pulse.setAttribute('r', n.r);
    pulse.style.animationDelay = n.delay + 's';
    g.appendChild(pulse);

    const circle = document.createElementNS(svgNS, 'circle');
    circle.setAttribute('class', 'kg-node ' + n.type);
    circle.setAttribute('cx', n.x);
    circle.setAttribute('cy', n.y);
    circle.setAttribute('r', n.r);
    g.appendChild(circle);

    const labelEl = document.createElementNS(svgNS, 'text');
    labelEl.setAttribute('class', 'kg-label');
    labelEl.setAttribute('x', n.x);
    labelEl.setAttribute('y', n.y + n.r + 16);
    labelEl.textContent = n.label;
    g.appendChild(labelEl);

    g.addEventListener('click', function (ev) {
      ev.stopPropagation();
      showKgPopup(n);
    });

    nodesGroup.appendChild(g);
  });

  if (popup && !popup.dataset.bound) {
    popup.dataset.bound = 'true';
    document.addEventListener('click', function (ev) {
      if (!popup.contains(ev.target)) {
        hideKgPopup();
      }
    });
    const closeBtn = popup.querySelector('.kg-popup-close');
    if (closeBtn) closeBtn.addEventListener('click', hideKgPopup);
  }
}

function showKgPopup(node) {
  const popup = document.getElementById('kgPopup');
  if (!popup) return;

  const titleEl = popup.querySelector('.kg-popup-title');
  const descEl = popup.querySelector('.kg-popup-desc');
  const linkEl = popup.querySelector('.kg-popup-link');

  titleEl.textContent = node.label;
  descEl.textContent = node.desc || '';

  if (node.link) {
    linkEl.href = node.link;
    linkEl.style.display = 'inline-flex';
  } else {
    linkEl.style.display = 'none';
  }

  popup.classList.add('open');
}

function hideKgPopup() {
  const popup = document.getElementById('kgPopup');
  if (popup) popup.classList.remove('open');
}